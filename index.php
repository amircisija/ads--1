        var timeout;
        $('.hero--section').mousemove(function (e) {
            if (timeout) clearTimeout(timeout);
            setTimeout(callParallax.bind(null, e), 200);

        });
        $('.pg-1').mousemove(function (e) {
            if (timeout) clearTimeout(timeout);
            setTimeout(callParallax.bind(null, e), 200);

